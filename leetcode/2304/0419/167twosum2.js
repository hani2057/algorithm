/**
 * @param {number[]} numbers
 * @param {number} target
 * @return {number[]}
 */
var twoSum = function (numbers, target) {
  let s = 0;
  let e = numbers.length - 1;

  while (s < e) {
    const sum = numbers[s] + numbers[e];
    if (sum < target) s++;
    else if (sum > target) e--;
    else if (sum === target) return [s + 1, e + 1];
  }
};
